#include<iostream>
using namespace std;

int main()
{
    int a, b;
    cout <<"Enter first number"<<endl;
    cin >>a;
    cout <<"Enter second number"<<endl;
    cin >>b;
    cout <<"The value of a+b is" <<a+b <<endl;
    cout <<"The value of a-b is" <<a-b <<endl;
    cout <<"The value of a*b is" <<a*b <<endl;
    cout <<"The value of a/b is" <<a/b <<endl;
    cout <<"The value of a/b is" << (float) a/b <<endl;//it is called typecasting of function to another one

}